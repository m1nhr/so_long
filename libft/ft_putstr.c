#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (!s)
	{
		*len += write(1, "(null)", 6);
		return ;
	}
	while (s[i])
	{
		*len += write(1, &s[i], 1);
		i++;
	}
}
